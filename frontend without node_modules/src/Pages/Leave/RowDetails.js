import React from 'react';
import { Card, Row, Col } from 'react-bootstrap';

const RowDetails = ({ row }) => {
    if (!row) return <p>Leave application not found.</p>;

    const leaveDetails = [
        {
            type: "Annual Leave",
            paymentState: row.annualLeavePaymentState || "Unknown",
            totalDays: row.annualLeaveTotalDays || "0/0",
        },
        {
            type: "Casual Leave",
            paymentState: row.casualLeavePaymentState || "Unknown",
            totalDays: row.casualLeaveTotalDays || "0/0",
        },
        {
            type: "Medical Leave",
            paymentState: row.medicalLeavePaymentState || "Unknown",
            totalDays: row.medicalLeaveTotalDays || "0/0",
        },
        {
            type: "Maternity Leave",
            paymentState: row.maternityLeavePaymentState || "Unknown",
            totalDays: row.maternityLeaveTotalDays || "0/0",
        },
    ];

    return (
        <div>
            <Card className="mx-2 mb-3">
                <Row className="justify-around p-2 text-center rounded-lg bg-light d-flex">
                    <h2><strong>Leave Details</strong></h2>
                    <div className='flex flex-col items-start'>
                        <h5>Name: {row.name}</h5>
                        <h5>Department: {row.department}</h5>
                    </div>
                    {leaveDetails.map((leave, index) => (
                        <Col lg={3} md={6} sm={12} key={index}>
                            <Card
                                border="info"
                                className="p-4 transition-transform duration-200 rounded-lg shadow-md cursor-pointer d-flex justify-content-center align-items-center h-100 hover:scale-105"
                            >
                                <h5 className="font-semibold text-blue-600">{leave.type}</h5>
                                <div className='flex flex-row'>
                                    <div style={{ border: "1px solid #ddd", padding: "10px", borderRadius: "5px", width: "150px" }}>
                                        <p style={{ fontWeight: "bold", margin: "0 0 5px" }}>Payment State</p>
                                        <small style={{ color: "green" }}>{leave.paymentState}</small>
                                    </div>
                                    <div>
                                        <div style={{ border: "1px solid #ddd", padding: "10px", borderRadius: "5px", width: "100px" }}>
                                            <p style={{ fontWeight: "bold", margin: "0 0 5px" }}>Total Days</p>
                                            <small style={{ color: "green" }}>{leave.totalDays}</small>
                                        </div>
                                    </div>
                                </div>
                            </Card>
                        </Col>
                    ))}
                </Row>
            </Card>
        </div>
    );
};

export default RowDetails;
